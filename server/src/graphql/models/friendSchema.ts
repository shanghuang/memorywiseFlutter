"use strict";
import mongoose from 'mongoose';
const friendSchema = new mongoose.Schema({
    firstName: {
        type: String
    },
    lastName: {
        type: String
    },
    gender: {
        type: String
    },
    age: {
        type: Number
    },
    language: {
        type: String
    },
    email: {
        type: String
    },
    contacts: {
        type: Array
    },
});

export default mongoose.models.FriendModel || mongoose.model('Friends', friendSchema);